public class VariableDemo2
{
	public static void main(String[] args)
	{
		//1.基本用法
		//定义变量，再进行输出
		int a =10;
		System.out.println(a);
		System.out.println(a);
		System.out.println(a);
		
		//2.变量参与计算
		int b=2;
		System.out.println(a+b);
		
		//3.修改变量记录的值
		a=50;
		System.out.println(a);
		
		
		//在一条语句中可以定义多个变量
		int d=100,e=200,f=300;
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		
		
	}
}